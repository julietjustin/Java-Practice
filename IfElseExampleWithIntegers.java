class IfElseExampleWithIntegers
{
	public static void main(String args[])
		{
		int a = Integer.parseInt(args[0]);
			{
			if (a > 10 )
				{
				System.out.println("The Number is greater than 10");
				}
			else if (a < 10)
			    {
				System.out.println("The Number is smaller than 10");
				}
			}
		}
}
				